func (t ItemType) String() string {
	s, found := items[t]
	if found {
		return s
	}
	return fmt.Sprintf("item%d", t)
}

// IconFile returns the suffix for the item's icon.
func (t ItemType) IconFile() string {
	s, found := itemIcons[t]
	if found {
		return s
	}
	return "unknown"
}
